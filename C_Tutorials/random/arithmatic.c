//C Program to demonstrate use of arithmatic operators
#include<stdio.h>
    int main(void)
    {
        int a,b;
        printf("Enter value of a--> ");
        scanf("%d",&a);
        printf("Enter value of b--> ");
        scanf("%d",&b);

        printf("Value of a + b --> %d\n",a+b);
        printf("Value of a - b --> %d\n",a-b);
        printf("Value of a * b --> %d\n",a*b);
        printf("Value of a / b --> %d\n",a/b);

    }
